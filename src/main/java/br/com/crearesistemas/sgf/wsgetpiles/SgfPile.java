package br.com.crearesistemas.sgf.wsgetpiles;

public class SgfPile {

	Long pileCode;
    String description;
    Long localeCode;
    Integer productCode;
    String firstMovementDate;
    String lineId;
    Integer density;
    String densityClass;
    Integer timeAfterCutting;
    String status;
	public Long getPileCode() {
		return pileCode;
	}
	public void setPileCode(Long pileCode) {
		this.pileCode = pileCode;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Long getLocaleCode() {
		return localeCode;
	}
	public void setLocaleCode(Long localeCode) {
		this.localeCode = localeCode;
	}
	public Integer getProductCode() {
		return productCode;
	}
	public void setProductCode(Integer productCode) {
		this.productCode = productCode;
	}
	public String getFirstMovementDate() {
		return firstMovementDate;
	}
	public void setFirstMovementDate(String firstMovementDate) {
		this.firstMovementDate = firstMovementDate;
	}
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public Integer getDensity() {
		return density;
	}
	public void setDensity(Integer density) {
		this.density = density;
	}
	public String getDensityClass() {
		return densityClass;
	}
	public void setDensityClass(String densityClass) {
		this.densityClass = densityClass;
	}
	public Integer getTimeAfterCutting() {
		return timeAfterCutting;
	}
	public void setTimeAfterCutting(Integer timeAfterCutting) {
		this.timeAfterCutting = timeAfterCutting;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
    	
    
    
	
}
