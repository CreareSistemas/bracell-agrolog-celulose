package br.com.crearesistemas.sgf.wsgetcranes;

public class SgfCrane {

	Long craneCode;
    String description;
    String abbreviation;
    String craneType;
    String status;
	public Long getCraneCode() {
		return craneCode;
	}
	public void setCraneCode(Long craneCode) {
		this.craneCode = craneCode;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAbbreviation() {
		return abbreviation;
	}
	public void setAbbreviation(String abbreviation) {
		this.abbreviation = abbreviation;
	}
	public String getCraneType() {
		return craneType;
	}
	public void setCraneType(String craneType) {
		this.craneType = craneType;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	    
	
}
