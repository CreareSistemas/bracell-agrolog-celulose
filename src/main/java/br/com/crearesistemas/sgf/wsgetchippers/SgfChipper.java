package br.com.crearesistemas.sgf.wsgetchippers;

public class SgfChipper {

	Long chipperCode;
    String description;
    String abbreviation;
    Long localeCode;
    String lineId;
    String status;
	public Long getChipperCode() {
		return chipperCode;
	}
	public void setChipperCode(Long chipperCode) {
		this.chipperCode = chipperCode;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAbbreviation() {
		return abbreviation;
	}
	public void setAbbreviation(String abbreviation) {
		this.abbreviation = abbreviation;
	}
	public Long getLocaleCode() {
		return localeCode;
	}
	public void setLocaleCode(Long localeCode) {
		this.localeCode = localeCode;
	}
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
    
	
}
